from statsmodels.stats.contingency_tables import mcnemar
import numpy as np
import pandas as pd

def test_mcnemar(y_verite, y_pred_modele1, y_pred_modele2, nom1, nom2):
    """
    Test de  pour comparer deux classificateurs

    Paramètres:
    -----------
    y_verite: array, les vraies étiquettes (0 ou 1)
    y_pred_modele1: array, prédictions du modèle 1
    y_pred_modele2: array, prédictions du modèle 2
    nom1: str, nom du modèle 1
    nom2: str, nom du modèle 2

    Retourne:
    ---------
    p_valeur: float, p-valeur du test
    """

    # Étape 1: Calculer les prédictions correctes pour chaque modèle
    predictions_correctes1 = (y_pred_modele1 == y_verite)
    predictions_correctes2 = (y_pred_modele2 == y_verite)

    # Étape 2: Compter les désaccords et accords
    # n11: Modèle 1 correct, Modèle 2 correct
    n11 = np.sum(predictions_correctes1 & predictions_correctes2)
    # n01: Modèle 1 correct, Modèle 2 incorrect
    n01 = np.sum(predictions_correctes1 & ~predictions_correctes2)
    # n10: Modèle 1 incorrect, Modèle 2 correct
    n10 = np.sum(~predictions_correctes1 & predictions_correctes2)
    # n00: Modèle 1 incorrect, Modèle 2 incorrect
    n00 = np.sum(~predictions_correctes1 & ~predictions_correctes2)

    # Étape 3: Construire le tableau de contingence pour McNemar
    # Format attendu: [[correct1_correct2, correct1_incorrect2], [incorrect1_correct2, incorrect1_incorrect2]]
    tableau = [[n11, n01],
               [n10, n00]]

    # Étape 4: Appliquer le test de McNemar
    resultat = mcnemar(tableau, exact=True)

    # Étape 5: Afficher les résultats
    print(f"\n{'='*80}")
    print(f"TEST DE MCNEMAR: {nom1} vs {nom2}")
    print(f"{'='*80}")
    print(f"\nDétails des prédictions:")
    print(f"  • Les deux modèles corrects (n11):        {n11} cas ({n11/len(y_verite)*100:.1f}%)")
    print(f"  • {nom1} correct, {nom2} incorrect (n01): {n01} cas ({n01/len(y_verite)*100:.1f}%)")
    print(f"  • {nom1} incorrect, {nom2} correct (n10): {n10} cas ({n10/len(y_verite)*100:.1f}%)")
    print(f"  • Les deux modèles incorrects (n00):      {n00} cas ({n00/len(y_verite)*100:.1f}%)")

    print(f"\nTest statistique:")
    print(f"  • P-valeur: {resultat.pvalue:.6f}")
    print(f"  • Statistique χ²: {resultat.statistic:.4f}")

    # Interprétation
    print(f"\nInterprétation:")
    if resultat.pvalue < 0.05:
        if n01 > n10:
            print(f"  CONCLUSION: {nom1} est SIGNIFICATIVEMENT meilleur (p < 0.05)")
        else:
            print(f"   CONCLUSION: {nom2} est SIGNIFICATIVEMENT meilleur (p < 0.05)")
        print(f"     → Les deux modèles ont des performances DIFFÉRENTES")
    else:
        print(f"  CONCLUSION: Pas de différence significative (p ≥ 0.05)")
        print(f"     → Les deux modèles ont des performances SIMILAIRES")
    print(f"{'='*80}\n")

    return resultat.pvalue


