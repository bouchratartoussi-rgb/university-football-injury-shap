# ── Données après correction VIF ────────────────────────────
# X_corrige et y sont déjà définis depuis l'étape 2

# ── Définition du 5-fold stratifié ──────────────────────────
# Stratifié = préserve la proportion 50/50 blessés/non-blessés
# dans chaque fold
cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

# ── Métriques à calculer ─────────────────────────────────────
scoring = {
    "accuracy" : "accuracy",
    "f1"       : "f1",
    "precision": "precision",
    "recall"   : "recall"
}

# ── Modèles dans un Pipeline (scaler inclus) ─────────────────
# Important : le StandardScaler est DANS le pipeline
# pour qu'il soit refait proprement à chaque fold
# sans fuite entre train et test

modeles = {
    "Régression Logistique": Pipeline([
        ("scaler", StandardScaler()),
        ("model", LogisticRegression(max_iter=1000, random_state=42))
    ]),
    "Random Forest": Pipeline([
        ("scaler", StandardScaler()),
        ("model", RandomForestClassifier(random_state=42))
    ]),
    "SVM": Pipeline([
        ("scaler", StandardScaler()),
        ("model", SVC(kernel="rbf", probability=True, random_state=42))
    ]),
    "XGBoost": Pipeline([
        ("scaler", StandardScaler()),
        ("model", XGBClassifier(eval_metric="logloss", random_state=42))
    ])
}

# ── Lancement Cross-Validation ───────────────────────────────
print(" Cross-Validation 5-fold en cours...\n")
resultats_cv = []

for nom, pipeline in modeles.items():
    scores = cross_validate(
        pipeline, X_corrige, y,
        cv=cv,
        scoring=scoring,
        return_train_score=False
    )
    resultats_cv.append({
        "Modèle"    : nom,
        "Accuracy"  : scores["test_accuracy"].mean().round(4),
        "Accuracy σ": scores["test_accuracy"].std().round(4),
        "F1"        : scores["test_f1"].mean().round(4),
        "F1 σ"      : scores["test_f1"].std().round(4),
        "Precision" : scores["test_precision"].mean().round(4),
        "Recall"    : scores["test_recall"].mean().round(4),
    })
    print(f" {nom} terminé")

# ── Tableau des résultats ────────────────────────────────────
df_cv = pd.DataFrame(resultats_cv)

print("\n" + "=" * 75)
print(" RÉSULTATS CROSS-VALIDATION 5-FOLD (données après VIF)")
print("=" * 75)
