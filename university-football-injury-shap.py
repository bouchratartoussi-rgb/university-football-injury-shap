def fig_bootstrap_distribution():
    """
    4-panel histogram of F1-score bootstrap distributions.
    Requires: scores_f1_svm, scores_f1_log, scores_f1_rf, scores_f1_xgb
              ci_low/high_f1_svm, ci_low/high_f1_log, etc.
    Save: fig_bootstrap_ci.png
    """
    models_boot = [
        ('SVM Optimised',       scores_f1_svm, ci_low_f1_svm, ci_high_f1_svm, COLORS['svm']),
        ('Logistic Regression', scores_f1_log, ci_low_f1_log, ci_high_f1_log, COLORS['log']),
        ('Random Forest',       scores_f1_rf,  ci_low_f1_rf,  ci_high_f1_rf,  COLORS['rf']),
        ('XGBoost',             scores_f1_xgb, ci_low_f1_xgb, ci_high_f1_xgb, COLORS['xgb']),
    ]

    fig, axes = plt.subplots(2, 2, figsize=(14, 9))
    fig.suptitle('Bootstrap Distribution of F1-Scores (1000 Iterations)',
                 fontsize=14, fontweight='bold')

    for ax, (name, scores, lo, hi, color) in zip(axes.flat, models_boot):
        mean_val = np.mean(scores)
        ax.hist(scores, bins=40, color=color, alpha=0.75, edgecolor='white')
        ax.axvline(mean_val, color='black', linewidth=2, linestyle='-',
                   label=f'Mean: {mean_val:.4f}')
        ax.axvline(lo, color='black', linewidth=1.5, linestyle='--',
                   label=f'95% CI: [{lo:.4f}, {hi:.4f}]')
        ax.axvline(hi, color='black', linewidth=1.5, linestyle='--')
        ax.set_title(name, fontsize=12, fontweight='bold')
        ax.set_xlabel('F1-Score', fontsize=10)
        ax.set_ylabel('Frequency', fontsize=10)
        ax.legend(fontsize=9, framealpha=0.9)
        ax.grid(True, alpha=0.3, linestyle='--')

    plt.tight_layout()
    plt.savefig('fig_bootstrap_ci.png')
    plt.show()
    print("✓ Saved: fig_bootstrap_ci.png")

fig_bootstrap_distribution()

