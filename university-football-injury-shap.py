    # Handle both 2D and 3D SHAP arrays
    sv = shap_values_svm
    if sv.ndim == 3:
        sv = sv[:, :, 1]   # class=1 (injured)

    mean_abs_shap = np.abs(sv).mean(axis=0)
    features = X_test_svm.columns.tolist()

    df_shap = pd.DataFrame({'feature': features, 'importance': mean_abs_shap})
    df_shap = df_shap.sort_values('importance', ascending=True)

