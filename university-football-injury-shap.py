def bootstrap_ci(y_true, y_pred, metric_func, n_iter=1000):
    """
    Calcul Confidence Interval pour une metrique

    Parametres:
    -----------
    y_true: array, les vraies etiquettes (0 ou 1)
    y_pred: array, les predictions
    metric_func: function, la metrique a calculer (f1_score, accuracy, etc)
    n_iter: int, nombre d'iterations (1000 = par defaut)

    Retourne:
    ---------
    ci_low: float, borne inferieure du CI
    ci_high: float, borne superieure du CI
    scores: list, tous les scores calcules
    """

    scores = []
    np.random.seed(42)  # pour la reproducibilite

    # Iteration 1000 fois
    for _ in range(n_iter):
        # Selectionner des indices aleatoires avec replacement
        idx = resample(np.arange(len(y_true)), random_state=None)

        # Calculer la metrique sur l'echantillon bootstrap
        score = metric_func(y_true.iloc[idx], y_pred[idx])
        scores.append(score)

    # Calculer les 2.5th et 97.5th percentiles
    ci_low = np.percentile(scores, 2.5)
    ci_high = np.percentile(scores, 97.5)

    return ci_low, ci_high, scores


